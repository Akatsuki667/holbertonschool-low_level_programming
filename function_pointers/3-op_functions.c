#include "3-calc.h"
/**
 * op_add - additionner paramètres
 * @a: valeur à additionner
 * @b: valeur à additionner
 * Return: addition des valeurs
 */
int op_add(int a, int b)
{
return (a + b);
}
/**
 * op_sub - soustraction paramètres
 * @a: valeur à soustraire
 * @b: valeur à soustraire
 * Return: soustraction des valeurs
 */
int op_sub(int a, int b)
{
return (a - b);
}
/**
 * op_mul - multiplication paramètres
 * @a: valeur à multiplier
 * @b: valeur à multiplier
 * Return: multiplication des valeurs
 */
int op_mul(int a, int b)
{
return (a * b);
}
/**
 * op_div - diviser paramètres
 * @a: valeur à diviser
 * @b: valeur à diviser
 * Return: diviser des valeurs
 */
int op_div(int a, int b)
{
return (a / b);
}
/**
 * op_mod - reste euclidien paramètres
 * @a: valeur à modulo
 * @b: valeur à modulo
 * Return: reste euclidien des valeurs
 */
int op_mod(int a, int b)
{
return (a % b);
}
